package assessments.assessment2.Question3.PartA;

public class Calculator {

    // I used a return statement to make the value reusable just in case

    // this is to add two integers
    public int add(int a, int b) {
        return a + b;
    }

    // this is for adding two doubles
    public double add(double a, double b) {
        return a + b;
    }

    // add three integers
    public int add(int a, int b, int c) {
        return a + b + c;
    }

    // this one concatenate two strings
    public String add(String a, String b) {
        return a + b;
    }
}
